@dataclass(frozen=True)
class AesKeyScheduler:
    initial_round: int
    desired_round: int
    key: bytes

    def schedule(self) -> bytes:
        key_list = list(self.key)
        is_aes256 = len(key_list) == 32

        if is_aes256:
            mod_desired_round = self.desired_round // 2
            mod_initial_round = self._get_initial_round(self.initial_round)
        else:
            mod_desired_round = self.desired_round
            mod_initial_round = self.initial_round

        if self.initial_round < self.desired_round:
            expanded = self._forward_expand(
                mod_initial_round, mod_desired_round, key_list
            )
        else:
            expanded = self._backward_expand(
                mod_initial_round, mod_desired_round, key_list
            )

        return bytes(self._slice_key_if_needed(expanded, self.desired_round))

    @staticmethod
    def _slice_key_if_needed(state: List[int], desired_round: int) -> List[int]:
        if len(state) == 32:
            return state[16:32] if desired_round % 2 else state[:16]
        return state

    @staticmethod
    def _get_initial_round(initial_round: int) -> int:
        if initial_round != 13 and initial_round % 2 != 0:
            raise ValueError("AES-256 initial round must be divisible by 2")
        return initial_round // 2 if initial_round != 13 else initial_round

    @staticmethod
    def _new_state(first_four: List[int], last_four: List[int], rcon: int) -> List[int]:
        rotated = AesKeyScheduler._rotate(last_four)
        substituted = [K_SBOX[x] for x in rotated]
        substituted[0] ^= rcon
        return [f ^ s for f, s in zip(first_four, substituted)]

    @staticmethod
    def _forward_expand(start: int, end: int, state: List[int]) -> List[int]:
        key_len = len(state)
        round_count = start
        while round_count < end:
            round_count += 1
            state[:4] = AesKeyScheduler._new_state(
                state[:4], state[-4:], K_RCON[round_count]
            )

            for i in range(4, key_len, 4):
                inp = state[i - 4 : i]
                if key_len == 32 and i == 16:
                    inp = [K_SBOX[x] for x in inp]
                state[i : i + 4] = [x ^ y for x, y in zip(state[i : i + 4], inp)]

        return state

    @staticmethod
    def _backward_expand(
        initial_round: int, desired_round: int, state: List[int]
    ) -> List[int]:
        key_len = len(state)
        round_count = initial_round

        while round_count > desired_round:
            if key_len == 32 and round_count == 13:
                round_count = 6
                old_state = state[16:32]
                state[16:32] = state[:16]
                for i in range(12, 0, -4):
                    state[i : i + 4] = [
                        x ^ y
                        for x, y in zip(old_state[i : i + 4], old_state[i - 4 : i])
                    ]
                state[:4] = AesKeyScheduler._new_state(
                    old_state[:4], state[-4:], K_RCON[7]
                )

            for i in range(key_len - 4, 0, -4):
                inp = state[i - 4 : i]
                if key_len == 32 and i == 16:
                    inp = [K_SBOX[x] for x in inp]
                state[i : i + 4] = [x ^ y for x, y in zip(state[i : i + 4], inp)]

            state[:4] = AesKeyScheduler._new_state(
                state[:4], state[-4:], K_RCON[round_count]
            )
            round_count -= 1

        return state

    @staticmethod
    def _rotate(block: List[int], to_left: bool = True) -> List[int]:
        if len(block) != 4:
            raise ValueError("Rotate supports only 4-byte blocks")
        return block[1:] + block[:1] if to_left else block[-1:] + block[:-1]


